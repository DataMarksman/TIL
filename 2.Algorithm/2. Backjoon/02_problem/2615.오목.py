# 설계 의도: 조건에 맞는 실행
# 1. 검은돌이 1, 흰돌이 2, 다 돌면 0 그대로 출력.
# 2. 패딩으로 사방을 두르고, 4방향을 기준으로, 이전 돌과 현재돌이 다르면 탐색 시작
# 3. while문으로 돌려서 같은 돌이 정확히 연속으로 5번만 나와야 winner로 선정.
# 개선점:
# 1. 효율화 하겠다고, 수평 범위를 N-4 로 줬다가 망함. 8번의 디버깅 동안 그 반례 한번 안넣은게 레전드

# 위 -> 아래, 좌상 -> 우하, 좌 -> 우, 좌하 -> 우상 4방향으로 탐색.
dx = [1, 1, 0, -1]
dy = [0, 1, 1, 1]

def checking(x, y):
    global winner
    global answer
    target = board[x][y]
    for direction in range(4):
        px = int(x)
        py = int(y)
        p_count = 0
        if board[px-dx[direction]][py-dy[direction]] != board[px][py]:
            p_count += 1
            while p_count < 6:
                px += dx[direction]
                py += dy[direction]
                if board[px][py] == target:
                    p_count += 1
                else:
                    break
            if p_count == 5:
                winner = target
                answer = (x, y)


N = 19
board = [[5]*21]
for put_in in range(19):
    lines = [5] + list(map(int, input().split())) + [5]
    board += [lines]
else:
    board += [[5]*21]

winner = 0
answer = (0, 0)
row = 1
while not winner and row < N+1:
    for col in range(1, 20):
        if board[row][col] == 1 or board[row][col] == 2:
            checking(row, col)
            if winner:
                break
    row += 1

if winner:
    print(winner)
    print(answer[0], answer[1])
else:
    print(winner)



